    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
       String id    = P_ID.getText().trim();
        String name  = P_name.getText().trim();
        String qty   = quantity.getText().trim();
        String img   = img2.getText().trim();
        String price = proice.getText().trim();
 
        if (id.isEmpty()||name.isEmpty()||qty.isEmpty()||img.isEmpty()||price.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Fill all fields!"); return;
        }
        try { Integer.parseInt(qty); Double.parseDouble(price); }
        catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,"Quantity and Price must be numbers!"); return;
        }
 
        try {
            java.io.File f = getProductsFile();
            System.out.println("[Admin] Saving to: " + f.getAbsolutePath());
            BufferedWriter bw = new BufferedWriter(new FileWriter(f, true));
            bw.write(id+","+name+","+qty+","+img+","+price);
            bw.newLine();
            bw.close();
 
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{id,name,qty,img,price});
            JOptionPane.showMessageDialog(this,"Saved Successfully!");
            P_ID.setText(""); P_name.setText(""); quantity.setText("");
            img2.setText(""); proice.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saving: "+e.getMessage());
        }
    }//GEN-LAST:event_AddActionPerformed
public void loadTable() {
    java.io.File f = getProductsFile();
        System.out.println("[Admin] Loading from: " + f.getAbsolutePath() + "  exists="+f.exists());
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            if (!f.exists()) return;
            BufferedReader br = new BufferedReader(new FileReader(f));
            String line;
            while ((line=br.readLine())!=null) {
                line=line.trim(); if(line.isEmpty()) continue;
                String[] d=line.split(",",5);
                if(d.length<5) continue;
                model.addRow(new Object[]{d[0],d[1],d[2],d[3],d[4]});
            }
            br.close();
        } catch (Exception e) {
            System.out.println("[Admin] Load error: "+e.getMessage());
        }
}
