    // ── Save entire table back to products.txt ────────────────────────────────
    private void saveTableToFile(DefaultTableModel model) {
       try {
            java.io.File f = getProductsFile();
            BufferedWriter bw = new BufferedWriter(new FileWriter(f));
            for (int i=0; i<model.getRowCount(); i++) {
                bw.write(model.getValueAt(i,0)+","+model.getValueAt(i,1)+","+
                         model.getValueAt(i,2)+","+model.getValueAt(i,3)+","+
                         model.getValueAt(i,4));
                bw.newLine();
            }
            bw.close();
            System.out.println("[Admin] Saved to: "+f.getAbsolutePath());
        } catch (Exception e) { e.printStackTrace(); }
    }
