    private void setupImageColumn() {
        // Increase row height for images
        jTable1.setRowHeight(80);
 
        // Column index 3 = Image path
        TableColumn imgCol = jTable1.getColumnModel().getColumn(3);
        imgCol.setCellRenderer(new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(
                    JTable table, Object value, boolean isSelected,
                    boolean hasFocus, int row, int column) {
 
                JLabel lbl = new JLabel();
                lbl.setHorizontalAlignment(SwingConstants.CENTER);
                lbl.setOpaque(true);
 
                if (isSelected) {
                    lbl.setBackground(table.getSelectionBackground());
                } else {
                    lbl.setBackground(row % 2 == 0 ? Color.WHITE : new Color(240, 248, 255));
                }
 
                if (value != null && !value.toString().trim().isEmpty()) {
                    try {
                        ImageIcon icon = new ImageIcon(value.toString().trim());
                        if (icon.getIconWidth() > 0) {
                            Image img = icon.getImage().getScaledInstance(70, 70, Image.SCALE_SMOOTH);
                            lbl.setIcon(new ImageIcon(img));
                        } else {
                            lbl.setText("No Image");
                        }
                    } catch (Exception ex) {
                        lbl.setText("No Image");
                    }
                } else {
                    lbl.setText("No Image");
                }
                return lbl;
            }
        });
    }
 
